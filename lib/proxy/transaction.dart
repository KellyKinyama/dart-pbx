/// Server transaction for INVITE (RFC 3261 §17.2.1). Handles 1xx/2xx/3xx-6xx
/// response transmission and ACK absorption, plus retransmit of final non-2xx
/// responses every Timer G doubling up to T2 until ACK or Timer H.
class InviteServerTransaction extends Transaction {
  InviteServerTransaction({
    required String id,
    required String branch,
    required TxSendFn send,
    required bool reliable,
  }) : super(
          kind: TxKind.inviteServer,
          id: id,
          branch: branch,
          method: 'INVITE',
          send: send,
          reliable: reliable,
        );

  int _gInterval = Timers.T1;
  Timer? _timerG;
  Timer? _timerH;
  Timer? _timerI;
  Timer? _timerL;

  /// Send an automatic 100 Trying generated by the proxy itself.
  void sendTrying(SipMsg request) {
    if (state != TxState.proceeding) return;
    final raw = buildResponse(request, code: 100, reason: 'Trying');
    lastResponse = raw;
    send(raw);
  }

  /// Send the constructed [response] (final or provisional) back to the UAC.
  void sendResponse(String response, int code) {
    lastResponse = response;
    send(response);
    if (code < 200) {
      // Stay in Proceeding.
      return;
    }
    if (code < 300) {
      if (state == TxState.accepted) return; // already absorbing 2xx retx
      // 2xx: per RFC 6026 we move to Accepted and absorb retransmits via Timer L.
      state = TxState.accepted;
      _timerL = Timer(const Duration(milliseconds: Timers.TIMER_L), terminate);
      _arm(_timerL!);
      return;
    }
    // 3xx-6xx: Completed, retransmit response on Timer G until ACK or Timer H.
    if (state == TxState.completed ||
        state == TxState.confirmed ||
        state == TxState.terminated) {
      // Already in completed: a duplicate final response from upstream
      // shouldn't reset our Timer G/H or move us out of confirmed/terminated.
      return;
    }
    state = TxState.completed;
    if (!reliable) {
      _scheduleG();
    }
    _timerH = Timer(const Duration(milliseconds: Timers.TIMER_H), () {
      // ACK never arrived; transport timeout.
      terminate();
    });
    _arm(_timerH!);
  }

  void _scheduleG() {
    _timerG = Timer(Duration(milliseconds: _gInterval), () {
      if (state != TxState.completed) return;
      if (lastResponse != null) send(lastResponse!);
      _gInterval = (_gInterval * 2).clamp(Timers.T1, Timers.T2);
      _scheduleG();
    });
    _arm(_timerG!);
  }

  /// Called when an INVITE retransmit arrives. Re-sends the last response if
  /// we are in Proceeding/Completed.
  void onRequestRetransmit() {
    if (state == TxState.proceeding || state == TxState.completed) {
      if (lastResponse != null) send(lastResponse!);
    }
  }

  /// Called when an ACK arrives (only meaningful for Completed-state).
  /// Returns true if the ACK was absorbed by this transaction (i.e. ACK to
  /// non-2xx). The proxy uses the return value to decide whether to forward.
  bool onAck() {
    if (state == TxState.completed) {
      _timerG?.cancel();
      _timerH?.cancel();
      state = TxState.confirmed;
      if (reliable) {
        terminate();
      } else {
        _timerI = Timer(const Duration(milliseconds: 5000), terminate);
        _arm(_timerI!);
      }
      return true; // absorbed — do not forward
    }
    // ACK to a 2xx is a brand-new transaction; let the proxy forward it.
    return false;
  }
}

/// Server transaction for non-INVITE methods (RFC 3261 §17.2.2).
class NonInviteServerTransaction extends Transaction {
  NonInviteServerTransaction({
    required String id,
    required String branch,
    required String method,
    required TxSendFn send,
    required bool reliable,
  }) : super(
          kind: TxKind.nonInviteServer,
          id: id,
          branch: branch,
          method: method,
          send: send,
          reliable: reliable,
        );

  Timer? _timerJ;

  void sendResponse(String response, int code) {
    lastResponse = response;
    send(response);
    if (code < 200) {
      state = TxState.proceeding;
      return;
    }
    state = TxState.completed;
    if (reliable) {
      terminate();
    } else {
      _timerJ = Timer(const Duration(milliseconds: Timers.TIMER_J), terminate);
      _arm(_timerJ!);
    }
  }

  void onRequestRetransmit() {
    if (state == TxState.proceeding || state == TxState.completed) {
      if (lastResponse != null) send(lastResponse!);
    }
  }
}

