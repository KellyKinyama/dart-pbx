/// Client transaction for INVITE (RFC 3261 §17.1.1). Retransmits the request
/// on Timer A doubling, gives up at Timer B, absorbs late responses on Timer D.
class InviteClientTransaction extends Transaction {
  InviteClientTransaction({
    required String id,
    required String branch,
    required TxSendFn send,
    required bool reliable,
    required this.request,
  }) : super(
          kind: TxKind.inviteClient,
          id: id,
          branch: branch,
          method: 'INVITE',
          send: send,
          reliable: reliable,
        );

  /// Raw INVITE that started this transaction. We keep it so we can build a
  /// matching ACK for non-2xx responses (RFC 3261 §17.1.1.3).
  final String request;

  int _aInterval = Timers.T1;
  Timer? _timerA;
  Timer? _timerB;
  Timer? _timerD;

  /// Callback fired on a final response (>=200). Receives the raw response
  /// string. The proxy uses this to forward the response upstream.
  void Function(String rawResponse, int code)? onFinalResponse;

  /// Callback for provisional responses (1xx, code != 100).
  void Function(String rawResponse, int code)? onProvisional;

  void start() {
    send(request);
    if (!reliable) _scheduleA();
    _timerB = Timer(const Duration(milliseconds: Timers.TIMER_B), () {
      // Transaction timeout; treat as 408.
      terminate();
    });
    _arm(_timerB!);
  }

  void _scheduleA() {
    _timerA = Timer(Duration(milliseconds: _aInterval), () {
      if (state != TxState.calling) return;
      send(request);
      _aInterval *= 2;
      _scheduleA();
    });
    _arm(_timerA!);
  }

  void onResponse(String raw, int code) {
    lastResponse = raw;
    if (code < 200) {
      _timerA?.cancel();
      state = TxState.proceeding;
      onProvisional?.call(raw, code);
      return;
    }

    // 2xx final.
    if (code < 300) {
      _timerA?.cancel();
      _timerB?.cancel();
      if (state == TxState.accepted || state == TxState.terminated) {
        // Duplicate 2xx — let the upper layer absorb retransmits, do not
        // refire onFinalResponse (would forward downstream twice).
        return;
      }
      state = TxState.accepted;
      onFinalResponse?.call(raw, code);
      _timerD = Timer(const Duration(milliseconds: Timers.TIMER_M), terminate);
      _arm(_timerD!);
      return;
    }

    // 3xx-6xx final. RFC 3261 §17.1.1.3: the ICT MUST generate an ACK and
    // send it to the same downstream hop as the INVITE. The ACK absorbs
    // upstream retransmits so the UAS stops re-sending the final.
    final ack = _buildAckForNon2xx(raw);
    send(ack);

    if (state == TxState.completed || state == TxState.terminated) {
      // A retransmitted non-2xx final. Re-send the ACK only (already done
      // above) and DO NOT refire onFinalResponse — that would push another
      // copy downstream, restart the IST's Timer G, and fork the storm.
      return;
    }

    _timerA?.cancel();
    _timerB?.cancel();
    state = TxState.completed;
    onFinalResponse?.call(raw, code);
    if (reliable) {
      terminate();
    } else {
      _timerD = Timer(const Duration(milliseconds: 32000), terminate);
      _arm(_timerD!);
    }
  }

  /// Builds an RFC 3261 §17.1.1.3 ACK for a non-2xx final response.
  /// Reuses the Via (with the original branch), Call-ID, From, To (with the
  /// response's To-tag), and CSeq number from the original INVITE.
  String _buildAckForNon2xx(String response) {
    String? toHeader;
    for (final line in response.split('\r\n')) {
      if (line.isEmpty) break;
      if (line.toLowerCase().startsWith('to:') ||
          line.toLowerCase().startsWith('t:')) {
        toHeader = line;
        break;
      }
    }

    final reqHeaderEnd = request.indexOf('\r\n\r\n');
    final reqHeaders =
        (reqHeaderEnd < 0 ? request : request.substring(0, reqHeaderEnd))
            .split('\r\n');
    if (reqHeaders.isEmpty) return request;

    // Request line: INVITE → ACK, keep Request-URI.
    reqHeaders[0] = reqHeaders[0].replaceFirst(RegExp(r'^INVITE\b'), 'ACK');

    final out = <String>[reqHeaders[0]];
    var seenContentLen = false;
    var seenTo = false;
    for (var i = 1; i < reqHeaders.length; i++) {
      final h = reqHeaders[i];
      final lower = h.toLowerCase();
      if (lower.startsWith('cseq:')) {
        // Same number, method = ACK.
        final num =
            h.substring(h.indexOf(':') + 1).trim().split(RegExp(r'\s+')).first;
        out.add('CSeq: $num ACK');
        continue;
      }
      if (lower.startsWith('content-length:')) {
        out.add('Content-Length: 0');
        seenContentLen = true;
        continue;
      }
      if ((lower.startsWith('to:') || lower.startsWith('t:')) &&
          toHeader != null) {
        out.add(toHeader); // pick up the To-tag from the response
        seenTo = true;
        continue;
      }
      // Drop any body-influencing headers, keep everything else (Via, From,
      // Call-ID, Max-Forwards, Route, etc.).
      if (lower.startsWith('content-type:')) continue;
      out.add(h);
    }
    if (!seenContentLen) out.add('Content-Length: 0');
    if (!seenTo && toHeader != null) out.add(toHeader);
    return '${out.join('\r\n')}\r\n\r\n';
  }
}

/// Client transaction for non-INVITE methods (RFC 3261 §17.1.2).
class NonInviteClientTransaction extends Transaction {
  NonInviteClientTransaction({
    required String id,
    required String branch,
    required String method,
    required TxSendFn send,
    required bool reliable,
    required this.request,
  }) : super(
          kind: TxKind.nonInviteClient,
          id: id,
          branch: branch,
          method: method,
          send: send,
          reliable: reliable,
        );

  final String request;

  int _eInterval = Timers.T1;
  Timer? _timerE;
  Timer? _timerF;
  Timer? _timerK;

  void Function(String rawResponse, int code)? onFinalResponse;
  void Function(String rawResponse, int code)? onProvisional;

  void start() {
    send(request);
    if (!reliable) _scheduleE();
    _timerF = Timer(const Duration(milliseconds: Timers.TIMER_F), terminate);
    _arm(_timerF!);
  }

  void _scheduleE() {
    _timerE = Timer(Duration(milliseconds: _eInterval), () {
      if (state == TxState.terminated) return;
      send(request);
      _eInterval = (_eInterval * 2).clamp(Timers.T1, Timers.T2);
      _scheduleE();
    });
    _arm(_timerE!);
  }

  void onResponse(String raw, int code) {
    lastResponse = raw;
    if (code < 200) {
      state = TxState.proceeding;
      onProvisional?.call(raw, code);
      return;
    }
    _timerE?.cancel();
    _timerF?.cancel();
    state = TxState.completed;
    onFinalResponse?.call(raw, code);
    if (reliable) {
      terminate();
    } else {
      _timerK = Timer(const Duration(milliseconds: Timers.T4), terminate);
      _arm(_timerK!);
    }
  }
}
