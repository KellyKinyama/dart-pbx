// SIP transaction state machines (RFC 3261 §17).
//
// Four flavours are supported:
//   * InviteServerTransaction      (§17.2.1) — Proceeding/Completed/Confirmed/Accepted/Terminated
//   * NonInviteServerTransaction   (§17.2.2) — Trying/Proceeding/Completed/Terminated
//   * InviteClientTransaction      (§17.1.1) — Calling/Proceeding/Completed/Accepted/Terminated
//   * NonInviteClientTransaction   (§17.1.2) — Trying/Proceeding/Completed/Terminated
//
// The implementation is intentionally compact: response/ACK retransmits are
// absorbed using the timer values from `lib/sip_2/timers.dart`. The transport
// layer is abstracted via a `send` callback rather than coupling to the
// existing `SipTransport` so unit tests can drive it without a real socket.

import 'dart:async';

import 'package:dart_pbx/sip_2/timers.dart';
import 'package:dart_pbx/sip_parser/sip.dart';

import 'sip_helpers.dart';

enum TxKind { inviteServer, nonInviteServer, inviteClient, nonInviteClient }

enum TxState {
  trying,
  calling,
  proceeding,
  completed,
  confirmed,
  accepted,
  terminated,
}

typedef TxSendFn = void Function(String rawMessage);

/// Common base for all four transaction types. Concrete subclasses are below.
abstract class Transaction {
  Transaction({
    required this.kind,
    required this.id,
    required this.branch,
    required this.method,
    required this.send,
    required this.reliable,
  }) : state = _initialState(kind);

  final TxKind kind;

  /// Unique key used by the transaction layer (see [TransactionLayer.keyFor]).
  final String id;

  /// Top-most Via branch (with magic cookie) captured at creation time.
  final String branch;

  /// Method this transaction was created for. ACK/CANCEL are matched against
  /// the original INVITE transaction; this field always holds the *original*
  /// method (e.g. "INVITE", never "ACK").
  final String method;

  /// Transport callback for sending bytes downstream / upstream.
  final TxSendFn send;

  /// True when running over a reliable transport (TCP/TLS/WS): suppresses the
  /// request retransmit Timer A and shortens absorb timers per §17.
  final bool reliable;

  TxState state;

  /// Last final/provisional response (for absorb-and-resend).
  String? lastResponse;

  /// Optional callback fired when the transaction reaches `terminated`.
  void Function()? onTerminate;

  final List<Timer> _timers = [];

  bool get isTerminated => state == TxState.terminated;

  void _arm(Timer t) => _timers.add(t);

  /// Cancel every armed timer and mark the transaction terminated.
  void terminate() {
    if (state == TxState.terminated) return;
    state = TxState.terminated;
    for (final t in _timers) {
      t.cancel();
    }
    _timers.clear();
    onTerminate?.call();
  }

  static TxState _initialState(TxKind k) {
    switch (k) {
      case TxKind.inviteServer:
        return TxState.proceeding;
      case TxKind.nonInviteServer:
        return TxState.trying;
      case TxKind.inviteClient:
        return TxState.calling;
      case TxKind.nonInviteClient:
        return TxState.trying;
    }
  }
}

